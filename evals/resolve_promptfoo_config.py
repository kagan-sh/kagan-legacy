#!/usr/bin/env python3
"""Resolve the promptfoo config to the best documented free GitHub Models model."""

from __future__ import annotations

import json
import os
import re
import subprocess
import sys
import urllib.request
from pathlib import Path
from typing import Final

CATALOG_URL: Final = "https://models.github.ai/catalog/models"
GITHUB_API_VERSION: Final = "2026-03-10"
DEFAULT_MODEL: Final = "openai/gpt-4.1"
MODEL_OVERRIDE_ENV: Final = "KAGAN_PROMPT_EVAL_MODEL"
TOKEN_ENV: Final = "GITHUB_TOKEN"
PREFERRED_MODELS: Final[tuple[str, ...]] = (
    "openai/gpt-4.1",
    "openai/gpt-4o",
    "openai/gpt-4.1-mini",
    "openai/gpt-4o-mini",
)

SCRIPT_DIR = Path(__file__).resolve().parent
SOURCE_CONFIG = SCRIPT_DIR / "promptfooconfig.yaml"
RESOLVED_CONFIG = SCRIPT_DIR / "promptfooconfig.resolved.yaml"


def main() -> int:
    override = os.getenv(MODEL_OVERRIDE_ENV)
    token = _read_token()

    if token is None:
        model = override or DEFAULT_MODEL
        print(
            f"No GitHub token available; using {model} without catalog validation.",
            file=sys.stderr,
        )
    else:
        available = _fetch_available_models(token)
        model = _select_model(available, override)

    resolved = _render_resolved_config(SOURCE_CONFIG.read_text(), model)
    RESOLVED_CONFIG.write_text(resolved)
    print(f"Resolved promptfoo model: {model}")
    print(f"Wrote {RESOLVED_CONFIG}")
    return 0


def _read_token() -> str | None:
    token = os.getenv(TOKEN_ENV)
    if token:
        return token

    try:
        token = subprocess.check_output(["gh", "auth", "token"], text=True).strip()
    except (FileNotFoundError, subprocess.CalledProcessError):
        return None

    return token or None


def _fetch_available_models(token: str) -> set[str]:
    request = urllib.request.Request(
        CATALOG_URL,
        headers={
            "Accept": "application/vnd.github+json",
            "Authorization": f"Bearer {token}",
            "X-GitHub-Api-Version": GITHUB_API_VERSION,
        },
    )
    with urllib.request.urlopen(request, timeout=20) as response:
        payload = json.load(response)
    return {item["id"] for item in payload if isinstance(item.get("id"), str)}


def _select_model(available: set[str], override: str | None) -> str:
    if override:
        if override not in available:
            raise SystemExit(
                f"{MODEL_OVERRIDE_ENV}={override!r} is not available to this token. "
                "Check https://models.github.ai/catalog/models or remove the override."
            )
        return override

    for model in PREFERRED_MODELS:
        if model in available:
            return model

    sample = ", ".join(sorted(model for model in available if model.startswith("openai/"))[:8])
    raise SystemExit(
        "None of the documented free defaults are available to this token. "
        f"Tried: {', '.join(PREFERRED_MODELS)}. "
        f"Available OpenAI models: {sample or 'none'}."
    )


def _render_resolved_config(source: str, model: str) -> str:
    source = re.sub(
        r"(^\s*-\s+id:\s+openai:chat:).+$",
        rf"\g<1>{model}",
        source,
        count=1,
        flags=re.MULTILINE,
    )
    source = re.sub(
        r"(^\s+label:\s+).+$",
        rf"\g<1>{model}",
        source,
        count=1,
        flags=re.MULTILINE,
    )
    return (
        "# Generated by evals/resolve_promptfoo_config.py. Do not edit.\n"
        f"# Selected model: {model}\n"
        f"{source}"
    )


if __name__ == "__main__":
    raise SystemExit(main())
